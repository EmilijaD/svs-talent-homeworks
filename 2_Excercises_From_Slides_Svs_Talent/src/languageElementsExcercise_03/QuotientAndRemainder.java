package languageElementsExcercise_03;

public class QuotientAndRemainder {

	public static void main(String[] args) {
		double number1 = 20;
		double number2 = 4;

		double resultDevide = 1;
		double resultModul;

		resultDevide = number1 / number2;
		resultModul = number1 % number2;

		System.out.println("Result by deviding is " + resultDevide + "\t"
				+ "Result by modulation is " + resultModul);

	}

}
